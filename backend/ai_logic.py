# რომელიც იყენებს ხელოვნურ ინტელექტს მომხმარებლის მოთხოვნის შინაარსობრივი ანალიზისთვის.
# სისტემის მთავარი უპირატესობაა მონაცემთა ლოკალური შენახვის ფუნქცია,
# რაც უზრუნველყოფს პროგრამის სწრაფ მუშაობას ყოველი მომდევნო ჩართვისას.

# მონაცემთა დამუშავება და მომზადება
# პროცესი იწყება load_data ფუნქციით,
# რომელიც კითხულობს tmdb_5000_movies.csv ფაილს.
# კოდი ავტომატურად განსაზღვრავს ფაილის გზას os ბიბლიოთეკის დახმარებით,
# ასუფთავებს მონაცემებს და ტოვებს მხოლოდ საჭირო სვეტებს: id,
# original_title და overview.

# ხელოვნური ინტელექტი და ვექტორიზაცია
# სისტემა იყენებს all-minilm-l6-v2 მოდელს,
# რომელიც sentence-transformers ბიბლიოთეკიდანაა.
# ეს მოდელი ფილმების აღწერებს გარდაქმნის მრავალგანზომილებიან რიცხვით ვექტორებად (embeddings).
# ეს ვექტორები საშუალებას აძლევს კომპიუტერს "გაიგოს" ფილმის შინაარსი და არა უბრალოდ მოძებნოს სიტყვების დამთხვევა.

# მუდმივი მონაცემთა ბაზა (persistent storage)
# პროექტის განახლებულ ვერსიაში გამოყენებულია chromadb.persistentclient.
# ეს ნიშნავს,
# რომ ვექტორული მონაცემთა ბაზა იქმნება არა დროებით მეხსიერებაში,
# არამედ ინახება მყარ დისკზე,
# კონკრეტულად backend/chroma_db ფოლდერში.

# კოდი მუშაობს ოპტიმიზებულად:
# სისტემა ჯერ ამოწმებს,
# არსებობს თუ არა უკვე შექმნილი ბაზა.
# თუ ბაზა ცარიელია,
# მხოლოდ მაშინ იწყება ფილმების აღწერების ვექტორებად გარდაქმნა (embedding process),
# რაც დიდ რესურსს მოითხოვს.
# თუ ბაზა უკვე არსებობს,
# სისტემა მას მომენტალურად ტვირთავს მყარი დისკიდან,
# რაც ზოგავს დროს და პროცესორის ენერგიას.

# ძიების მექანიზმი
# ფუნქცია search_movies მომხმარებლის მიერ შეყვანილ ნებისმიერ ფრაზას გარდაქმნის ვექტორად და chromadb-ს მეშვეობით პოულობს იმ ფილმებს,
# რომელთა ვექტორებიც სივრცეში ყველაზე ახლოს დგას მოთხოვნის ვექტორთან.
# ძიების შედეგად, სისტემა აბრუნებს ობიექტების სიას, რომელიც მოიცავს:
#- ფილმის სათაურს.
#- ფილმის მოკლე აღწერას (Overview).
#- სემანტიკური მსგავსების ქულას (Match Score), რომელიც გამოითვლება ვექტორებს შორის მანძილის საფუძველზე.
#ეს მიდგომა საშუალებას იძლევა, ფრონტენდზე აიგოს დინამიური "ბარათები",
#სადაც მომხმარებელი ხედავს არა მხოლოდ სახელს, არამედ იმასაც, თუ რამდენად ზუსტად პასუხობს ნაპოვნი ფილმი მის მოთხოვნას.


# ეს სისტემა არის სრულყოფილი სემანტიკური საძიებო სისტემა.
# მონაცემთა ლოკალური შენახვის (persistent storage) დამატებით,
# პროექტი გახდა უფრო პროფესიონალური და მოსახერხებელი,
# რადგან მომხმარებელს აღარ უწევს ლოდინი მონაცემთა ბაზის ხელახალ მომზადებაზე პროგრამის ყოველი გაშვებისას.

#შემოაქვს pandas ბიბლიოთეკა მონაცემთა ცხრილებთან სამუშაოდ.
import pandas as pd
#შემოაქვს chromadb ვექტორული მონაცემთა ბაზის სამართავად.
import chromadb
#შემოაქვს sentence_transformers ტექსტის ვექტორებად გარდასაქმნელად.
from sentence_transformers import SentenceTransformer
#შემოაქვს os ბიბლიოთეკა ფაილურ სისტემასთან სამუშაოდ.
import os


#ფუნქცია,
#რომელიც კითხულობს ფილმების მონაცემებს csv ფაილიდან.
def load_data():

    #პოულობს მიმდინარე ფაილის საქაღალდის გზას.
    base_dir=os.path.dirname(os.path.abspath(__file__))
    #ქმნის სრულ გზას მონაცემთა csv ფაილამდე.
    csv_path=os.path.join(base_dir, "..", "data", "tmdb_5000_movies.csv")

    #კითხულობს csv ფაილს და ინახავს მას dataframe-ში.
    df=pd.read_csv(csv_path)
    #ტოვებს მხოლოდ საჭირო სვეტებს და შლის ცარიელ სტრიქონებს.
    df=df[["id", "original_title", "overview"]].dropna()

    #აბრუნებს გასუფთავებულ მონაცემებს.
    return df


#ბეჭდავს შეტყობინებას მონაცემების ჩატვირთვის შესახებ.
print("Data and AI model loading...")
#იძახებს ფუნქციას და ინახავს მონაცემებს ცვლადში.
df=load_data()


#ტვირთავს კონკრეტულ ai მოდელს ტექსტის ვექტორიზაციისთვის.
model=SentenceTransformer("all-MiniLM-L6-v2")


#განსაზღვრავს ძირითად საქაღალდეს ბაზის შესანახად.
base_dir=os.path.dirname(os.path.abspath(__file__))
#ადგენს გზას,
#სადაც განთავსდება ვექტორული ბაზის ფაილები.
db_path=os.path.join(base_dir, "chroma_db")


#ქმნის მუდმივ კლიენტს,
#რომელიც მონაცემებს მყარ დისკზე ინახავს.
chroma_client=chromadb.PersistentClient(path=db_path)


#ქმნის ახალ კოლექციას ან ტვირთავს არსებულს ბაზიდან.
collection=chroma_client.get_or_create_collection(name="movies_collection")


#ამოწმებს,
#არის თუ არა ბაზაში უკვე ჩაწერილი მონაცემები.
if collection.count()==0:

    #ბეჭდავს შეტყობინებას,
    #რომ იწყება ვექტორიზაციის პროცესი.
    print("Now starts the embedding process. Please wait...")

    #ქმნის ფილმების სათაურების სიას.
    titles=df['original_title'].tolist()
    #ქმნის ფილმების აღწერების სიას.
    overviews=df["overview"].tolist()
    #ქმნის ფილმების id-ების სიას ტექსტურ ფორმატში.
    ids=[str(i) for i in df["id"].tolist()]

    #გარდაქმნის ყველა აღწერას მათემატიკურ ვექტორად.
    embeddings=model.encode(overviews)

    #ამატებს ვექტორებს,
    #ტექსტებს და სათაურებს მონაცემთა ბაზაში.
    collection.add(
        embeddings=embeddings.tolist(),
        documents=overviews,
        metadatas=[{"title": t} for t in titles],
        ids=ids
    )

    #ბეჭდავს შეტყობინებას პროცესის წარმატებით დასრულებაზე.
    print("Database is filled and saved to disk!")

else:
    #ბეჭდავს ინფორმაციას უკვე არსებული ბაზის ჩატვირთვის შესახებ.
    print(f"Database loaded from disk! Total movies: {collection.count()}")


#ფუნქცია,
#რომელიც ახორციელებს ფილმების ძებნას.
def search_movies(query, top_k=5):
    # გარდაქმნის მომხმარებლის მოთხოვნას ვექტორად.
    query_embedding = model.encode([query]).tolist()

    # ეძებს ბაზაში მოთხოვნის ყველაზე მსგავს ვექტორებს.
    results = collection.query(
        query_embeddings=query_embedding,
        n_results=top_k
    )

    formatted_results = []
    
    # ინდექსებით გადავუყვეთ შედეგებს, რომ ამოვიღოთ ყველა საჭირო ველი
    for i in range(len(results["ids"][0])):
        formatted_results.append({
            "title": results["metadatas"][0][i]["title"],
            "overview": results["documents"][0][i], # აქ ინახება ფილმის აღწერა
            "score": 1 - results["distances"][0][i] # გარდავქმნით დისტანციას მსგავსების ქულად
        })

    return formatted_results


#ამოწმებს,
#გაშვებულია თუ არა ფაილი პირდაპირ.
if __name__=="__main__":
    #ბეჭდავს ტესტური ძებნის შედეგებს კონსოლში.
    print(search_movies("A movie about love and friendship"))